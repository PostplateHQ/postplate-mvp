function asString(value, fallback = '') {
  const normalized = String(value || '').trim();
  return normalized || fallback;
}

function hashSeed(value = '') {
  let hash = 0;
  const input = String(value || '');
  for (let index = 0; index < input.length; index += 1) {
    hash = ((hash << 5) - hash) + input.charCodeAt(index);
    hash |= 0;
  }
  return Math.abs(hash);
}

function appendVariation(url, variation = 0) {
  const source = asString(url);
  if (!source) return '';
  try {
    const parsed = new URL(source);
    parsed.searchParams.set('ppv', String(variation));
    if (parsed.hostname.includes('unsplash.com')) parsed.searchParams.set('sig', String(variation));
    return parsed.toString();
  } catch (_error) {
    const glue = source.includes('?') ? '&' : '?';
    return `${source}${glue}ppv=${encodeURIComponent(String(variation))}`;
  }
}

function dynamicQueryFallback(itemDescription = '', cuisine = 'food', compositionType = 'hero_combo', variation = 0) {
  const normalizedItem = asString(itemDescription, 'restaurant food').replaceAll('_', ' ');
  const compositionHint = compositionType.replaceAll('_', ' ');
  const query = encodeURIComponent(`${normalizedItem} ${cuisine} ${compositionHint} food`);
  return `https://source.unsplash.com/1200x1200/?${query}&sig=${variation}`;
}

function flattenPool(group = {}) {
  return Object.values(group)
    .flatMap((entry) => (Array.isArray(entry) ? entry : [entry]))
    .filter(Boolean);
}

function cuisineAwareFallback(cuisine = 'american', compositionType = 'hero_combo', usedUrls = new Set(), options = {}) {
  const itemKey = asString(options.itemKey).toLowerCase();
  const itemGroup = STOCK_BY_ITEM_AND_COMPOSITION[itemKey] || null;
  const group = itemGroup || STOCK_BY_CUISINE_AND_COMPOSITION[cuisine] || STOCK_BY_CUISINE_AND_COMPOSITION.generic;
  const pool = Array.isArray(group[compositionType]) && group[compositionType].length
    ? group[compositionType]
    : (Array.isArray(group.hero_combo) ? group.hero_combo : []);
  const seedInput = `${asString(options.seed)}|${asString(options.itemDescription)}|${cuisine}|${compositionType}|${Date.now()}`;
  const seed = hashSeed(seedInput);
  const rotation = pool.length ? seed % pool.length : 0;

  for (let offset = 0; offset < pool.length; offset += 1) {
    const candidate = appendVariation(pool[(rotation + offset) % pool.length], seed + offset);
    if (!usedUrls.has(candidate)) return candidate;
  }

  const genericPool = flattenPool(STOCK_BY_CUISINE_AND_COMPOSITION.generic);
  for (let index = 0; index < genericPool.length; index += 1) {
    const candidate = appendVariation(genericPool[(seed + index) % genericPool.length], seed + index + 11);
    if (!usedUrls.has(candidate)) return candidate;
  }

  return dynamicQueryFallback(options.itemDescription, cuisine, compositionType, seed % 1000);
}

